    private void BTBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTBuscarActionPerformed
        PessoaDAO con = new PessoaDAO();
        
       try{
            Pessoa p = con.buscar(Long.parseLong(JTCodigo.getText()));
            JOptionPane.showMessageDialog(rootPane, "ID: " + p.getId() + "\nNome: " + p.getNome() + "\nSobrenome: " + p.getSobrenome());
       }catch(Exception e){
           JOptionPane.showMessageDialog(rootPane, "Problema ao procurar registro: " + e.getMessage());
       }
    }//GEN-LAST:event_BTBuscarActionPerformed

